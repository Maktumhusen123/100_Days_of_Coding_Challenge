# simple function
def greet():
    print("Hello")
    print("Welcome")
    print("Good evening")

# Function that allows for input
def greet_with_parameters(name,language):               # name and language are parameter
    print(f"Hello {name}")
    print(f"Welcomet to {language} programming")
    print("Good evening")

# calling simple function
greet()

# Calling function with Parameters
greet_with_parameters("Maktum","Python")            # Maktum and Python are arguments passed to the function
