# print() function is used to print the message
print("Welcome to Python Bootcamp")
print("Hello World!")

# + is used to concatenate strings
print("This is" + " a simple " + "program.")

# input() function is used to take input from the user
print("Hello " + input("What is your name"))

# len() is used to get length of string
print(len(input("What is your favourite pet?")))


# variables are used as container for storing values
name = input("What is your Name: ")
age = input("What is your Age: ")
print("Your name is " + name + " and You are " + age + " Years old.")
